<h2>Bienvenue sur le site de gestion des stages</h2>
	
<h3>Objectifs</h3>
<p>L'UF vise à sensibiliser les étudiants aux activités de recherche par le biais de « projets tutorés » (PT) effectués par groupe d'au moins 4 étudiants sous la conduite d'un tuteur (enseignant ou industriel).</p>
<p>Ces projets sont adossés à une formation à la recherche documentaire (FRD). Un cours de conduite de projet sert de guide à la réalisation du projet.</p>

<p>A la fin de ce module, l'étudiant devra avoir compris et pourra expliquer (principaux concepts) :</p>
<ul>
  <li>Les concepts, normes et techniques liées à l'élaboration d'un état de l'art attenant au projet</li>
  <li>Les concepts et techniques attenant à la conduite d'un projet en groupe</li>
</ul>

<p>L'étudiant devra être capable de :</p>
<ul>
  <li>Elaborer un état de l'art sur un domaine de recherche attenant au projet</li>
  <li>Conduire un projet en groupe</li>
  <li>Intégrer des techniques relevant de différents domaines pour aboutir à la réalisation demandée</li>
</ul>

<h3>Pré-requis</h3>

<p>Fontions du sujet du projet.</p>

<h3>Evaluation</h3>

<p>L'évaluation des acquis d'apprentissage est réalisée en continu tout le long du semestre.</p>
<p>En fonction des enseignements, elle peut prendre différentes formes : examen écrit, oral, compte-rendu, rapport écrit, évaluation par les pairs...</p>

